// sprite_rom.v
// 1-bit sprite ROM for Space Invaders
// All sprites are 16 wide x 8 tall (1x resolution, rendered at 2x in pixel_gen)
//
// sprite_id  (5-bit):
//   0        = player cannon
//   1        = squid   (top row),    frame 0
//   2        = squid   (top row),    frame 1
//   3        = crab    (middle row), frame 0
//   4        = crab    (middle row), frame 1
//   5        = octopus (bottom row), frame 0
//   6        = octopus (bottom row), frame 1
//   7 - 16   = digit sprites '0' through '9' (5-pixel-wide font, left-justified)
//   17       = explosion starburst (8-pixel wide, left-justified)
//
// pixel = 1 -> draw colour,  0 -> transparent
// MSB (bit 15) = leftmost pixel (col 0)
//
// case index = {sprite_id[4:0], row[2:0]}  (8-bit, 0-143)

`timescale 1ns / 1ps

module sprite_rom (
    input  wire [4:0] sprite_id,
    input  wire [2:0] row,       // 0-7
    input  wire [3:0] col,       // 0-15
    output wire       pixel
);

    reg [15:0] row_data;

    always @(*) begin
        case ({sprite_id, row})   // 8-bit index

            // -----------------------------------------------------------------
            // Player cannon  (id = 0)
            // -----------------------------------------------------------------
            8'd0:  row_data = 16'b0000001000000000;
            8'd1:  row_data = 16'b0000011100000000;
            8'd2:  row_data = 16'b0000011100000000;
            8'd3:  row_data = 16'b0011111111100000;
            8'd4:  row_data = 16'b0111111111110000;
            8'd5:  row_data = 16'b0111111111110000;
            8'd6:  row_data = 16'b0110001000110000;
            8'd7:  row_data = 16'b0000000000000000;

            // -----------------------------------------------------------------
            // Squid - frame 0  (id = 1)
            // -----------------------------------------------------------------
            8'd8:  row_data = 16'b0000011000000000;
            8'd9:  row_data = 16'b0001111110000000;
            8'd10: row_data = 16'b0011111111000000;
            8'd11: row_data = 16'b0110110110000000;
            8'd12: row_data = 16'b0011111111000000;
            8'd13: row_data = 16'b0001001001000000;
            8'd14: row_data = 16'b0010000000100000;
            8'd15: row_data = 16'b0001000010000000;

            // -----------------------------------------------------------------
            // Squid - frame 1  (id = 2)
            // -----------------------------------------------------------------
            8'd16: row_data = 16'b0000011000000000;
            8'd17: row_data = 16'b0001111110000000;
            8'd18: row_data = 16'b0011111111000000;
            8'd19: row_data = 16'b0110110110000000;
            8'd20: row_data = 16'b0011111111000000;
            8'd21: row_data = 16'b0001001001000000;
            8'd22: row_data = 16'b0100100010010000;
            8'd23: row_data = 16'b1000000000001000;

            // -----------------------------------------------------------------
            // Crab - frame 0  (id = 3)
            // -----------------------------------------------------------------
            8'd24: row_data = 16'b0000100010000000;
            8'd25: row_data = 16'b0000010100000000;
            8'd26: row_data = 16'b0001111111000000;
            8'd27: row_data = 16'b0011011011100000;
            8'd28: row_data = 16'b0111111111110000;
            8'd29: row_data = 16'b0101111111010000;
            8'd30: row_data = 16'b0100000000010000;
            8'd31: row_data = 16'b0010000000100000;

            // -----------------------------------------------------------------
            // Crab - frame 1  (id = 4)
            // -----------------------------------------------------------------
            8'd32: row_data = 16'b0000100010000000;
            8'd33: row_data = 16'b0010010100100000;
            8'd34: row_data = 16'b0011111111100000;
            8'd35: row_data = 16'b0111011011110000;
            8'd36: row_data = 16'b1111111111111000;
            8'd37: row_data = 16'b0101111111010000;
            8'd38: row_data = 16'b0001000000010000;
            8'd39: row_data = 16'b0100000000001000;

            // -----------------------------------------------------------------
            // Octopus - frame 0  (id = 5)
            // -----------------------------------------------------------------
            8'd40: row_data = 16'b0001111110000000;
            8'd41: row_data = 16'b0111111111100000;
            8'd42: row_data = 16'b1111111111110000;
            8'd43: row_data = 16'b1101011010110000;
            8'd44: row_data = 16'b1111111111110000;
            8'd45: row_data = 16'b0011001100110000;
            8'd46: row_data = 16'b0110000001100000;
            8'd47: row_data = 16'b0011000011000000;

            // -----------------------------------------------------------------
            // Octopus - frame 1  (id = 6)
            // -----------------------------------------------------------------
            8'd48: row_data = 16'b0001111110000000;
            8'd49: row_data = 16'b0111111111100000;
            8'd50: row_data = 16'b1111111111110000;
            8'd51: row_data = 16'b1101011010110000;
            8'd52: row_data = 16'b1111111111110000;
            8'd53: row_data = 16'b0100110011010000;
            8'd54: row_data = 16'b1001000000100000;
            8'd55: row_data = 16'b0100000001000000;

            // -----------------------------------------------------------------
            // Digit '0'  (id = 7)  -- 5-pixel wide font, cols 0-4 (bits 15-11)
            // -----------------------------------------------------------------
            8'd56: row_data = 16'h7000; // 01110
            8'd57: row_data = 16'h8800; // 10001
            8'd58: row_data = 16'h8800; // 10001
            8'd59: row_data = 16'h8800; // 10001
            8'd60: row_data = 16'h8800; // 10001
            8'd61: row_data = 16'h8800; // 10001
            8'd62: row_data = 16'h7000; // 01110
            8'd63: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Digit '1'  (id = 8)
            // -----------------------------------------------------------------
            8'd64: row_data = 16'h2000; // 00100
            8'd65: row_data = 16'h6000; // 01100
            8'd66: row_data = 16'h2000; // 00100
            8'd67: row_data = 16'h2000; // 00100
            8'd68: row_data = 16'h2000; // 00100
            8'd69: row_data = 16'h2000; // 00100
            8'd70: row_data = 16'h7000; // 01110
            8'd71: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Digit '2'  (id = 9)
            // -----------------------------------------------------------------
            8'd72: row_data = 16'h7000; // 01110
            8'd73: row_data = 16'h8800; // 10001
            8'd74: row_data = 16'h0800; // 00001
            8'd75: row_data = 16'h3000; // 00110
            8'd76: row_data = 16'h4000; // 01000
            8'd77: row_data = 16'h8000; // 10000
            8'd78: row_data = 16'hF800; // 11111
            8'd79: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Digit '3'  (id = 10)
            // -----------------------------------------------------------------
            8'd80: row_data = 16'h7000; // 01110
            8'd81: row_data = 16'h8800; // 10001
            8'd82: row_data = 16'h0800; // 00001
            8'd83: row_data = 16'h3000; // 00110
            8'd84: row_data = 16'h0800; // 00001
            8'd85: row_data = 16'h8800; // 10001
            8'd86: row_data = 16'h7000; // 01110
            8'd87: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Digit '4'  (id = 11)
            // -----------------------------------------------------------------
            8'd88: row_data = 16'h1000; // 00010
            8'd89: row_data = 16'h3000; // 00110
            8'd90: row_data = 16'h5000; // 01010
            8'd91: row_data = 16'h9000; // 10010
            8'd92: row_data = 16'hF800; // 11111
            8'd93: row_data = 16'h1000; // 00010
            8'd94: row_data = 16'h1000; // 00010
            8'd95: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Digit '5'  (id = 12)
            // -----------------------------------------------------------------
            8'd96:  row_data = 16'hF800; // 11111
            8'd97:  row_data = 16'h8000; // 10000
            8'd98:  row_data = 16'h8000; // 10000
            8'd99:  row_data = 16'hF000; // 11110
            8'd100: row_data = 16'h0800; // 00001
            8'd101: row_data = 16'h0800; // 00001
            8'd102: row_data = 16'hF000; // 11110
            8'd103: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Digit '6'  (id = 13)
            // -----------------------------------------------------------------
            8'd104: row_data = 16'h7000; // 01110
            8'd105: row_data = 16'h8000; // 10000
            8'd106: row_data = 16'h8000; // 10000
            8'd107: row_data = 16'hF000; // 11110
            8'd108: row_data = 16'h8800; // 10001
            8'd109: row_data = 16'h8800; // 10001
            8'd110: row_data = 16'h7000; // 01110
            8'd111: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Digit '7'  (id = 14)
            // -----------------------------------------------------------------
            8'd112: row_data = 16'hF800; // 11111
            8'd113: row_data = 16'h0800; // 00001
            8'd114: row_data = 16'h1000; // 00010
            8'd115: row_data = 16'h2000; // 00100
            8'd116: row_data = 16'h4000; // 01000
            8'd117: row_data = 16'h4000; // 01000
            8'd118: row_data = 16'h4000; // 01000
            8'd119: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Digit '8'  (id = 15)
            // -----------------------------------------------------------------
            8'd120: row_data = 16'h7000; // 01110
            8'd121: row_data = 16'h8800; // 10001
            8'd122: row_data = 16'h8800; // 10001
            8'd123: row_data = 16'h7000; // 01110
            8'd124: row_data = 16'h8800; // 10001
            8'd125: row_data = 16'h8800; // 10001
            8'd126: row_data = 16'h7000; // 01110
            8'd127: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Digit '9'  (id = 16)
            // -----------------------------------------------------------------
            8'd128: row_data = 16'h7000; // 01110
            8'd129: row_data = 16'h8800; // 10001
            8'd130: row_data = 16'h8800; // 10001
            8'd131: row_data = 16'h7800; // 01111
            8'd132: row_data = 16'h0800; // 00001
            8'd133: row_data = 16'h8800; // 10001
            8'd134: row_data = 16'h7000; // 01110
            8'd135: row_data = 16'h0000;

            // -----------------------------------------------------------------
            // Explosion starburst  (id = 17) -- 8-pixel wide, left-justified
            // -----------------------------------------------------------------
            8'd136: row_data = 16'h4400; // 01000100
            8'd137: row_data = 16'h1000; // 00010000  (centre dot)
            8'd138: row_data = 16'h8100; // 10000001  (corners)
            8'd139: row_data = 16'h2800; // 00101000  (diagonals)
            8'd140: row_data = 16'h8100; // 10000001
            8'd141: row_data = 16'h1000; // 00010000
            8'd142: row_data = 16'h4400; // 01000100
            8'd143: row_data = 16'h0000;

            default: row_data = 16'd0;
        endcase
    end

    // MSB = col 0 (leftmost pixel)
    assign pixel = row_data[15 - col];

endmodule
